// Переменные char ведут себя подобно целым числам.
class CharDemo2 {
    public static void main (String[] args) {
        char chl;
        chl='X';
        System.out.println("chl содержит"+chl);
        chl++; // инкрементировать chl
        System.out.println("chl теперь содержит"+chl);
    }
}